package task_5_feb;

public class task_32 {
    //Print even numbers from 1 to 20
    public static void main(String[] args)
    {
        int num=1;
        while(num<=20)
        {
            if(num%2==0)
            {
                System.out.println(num);
            }
            num++;
        }
    }
}
