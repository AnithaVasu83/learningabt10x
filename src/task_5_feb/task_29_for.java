package task_5_feb;

public class task_29_for {
    public static void main(String[] args)
    {
        //Print the sum of first 10 natural numbers
        int sum=0;
        for(int i=1;i<=10;i++)
        {
          sum +=i;

        }
        System.out.println("Sum of first 10 natural numbers: "+sum);
    }
}
