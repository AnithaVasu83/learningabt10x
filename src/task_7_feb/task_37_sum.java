package task_7_feb;
//Calculate the sum of numbers from 1 to 100
public class task_37_sum {
    public static void main(String[] args)
    {
        int sum=0;
        for(int i=1;i<=100;i++)
        {
            sum += i;

        }
        System.out.println("Sum of number from 1 to 100: "+sum);
    }
}
