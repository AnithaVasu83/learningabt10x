package ex2_operators;

public class Lab006_arithmetic_operator {
    public static void main(String[] args)
    {
        int a= 10;
        int b=3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        float c=10.31f;
        int d=3;
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(c % d);

    }
}
